<h3> online shopping carts compared</h3>
<p>Ecommerce shopping carts are like engines that keep online stores running smoothly. You'll be taking your customers along for the ride, so you'll want to choose an ecommerce shopping cart that not only meets your needs, but theirs as well. While your unique goals and budget will determine which ecommerce shopping cart is right for your online store, certain ecommerce shopping cart features can help you boost customer confidence and encourage repeat sales. </p> <p>There are several different types of ecommerce online shopping cart and each with their own advantages and disadvantages: </p>

<ol>
<li><h4>ASP (Active Server Pages) Shopping Cart Software:</h4></li>
<ul>
<li>Pros:  easy to build, edit and manage (the language was written by Microsoft and provides a robust ecommerce platform.</li>
<li>Cons: Cost ineffective, slower, flatform compatibility</li>
</ul>

<li><h4>CGI Shopping Cart:</h4></li>
<ul>
<li>Pros: they are supported by all of the major browsers. </li>
<li>Cons: hard to set-up and maintain, can be slow, and waste bandwidth and system resources.</li>
</ul>

<li><h4>Cookie Shopping Cart:</h4></li>
<ul>
<li>Pros: easy to setup, maintain and faster than traditional CGI shopping cart.  </li>
<li>Cons:  not supported by browsers that do not support cookies, and become ineffective in cases where the user chooses to turn off cookies in their browsers.</li>
</ul>

<li><h4>Java Shopping Cart:</h4></li>

<ul>
<li>Pros: a content rich and visually pleasing interface (and not limited by HTML). </li>
<li>Cons: The limitations of this shopping cart software are that since you have to build your store inside the applet, they are hard to customize and can be slow.</li>
</ul>

<li><h4>JavaScript Shopping Cart:</h4></li>
<ul>
<li>Pros :  fast, easy to set-up and maintain, easy to customize, save bandwidth, and save system resources. </li>
<li>Cons : not supported by browsers that do not support cookies and JavaScript, difficult to set-up large shopping carts.</li>
</ul>
</ol>

